"""# **Inferência — Segmentação da Imagem Completa**

Carregamos o melhor modelo salvo e realizamos a inferência
por sliding window (janela deslizante) na imagem inteira.

A soma dos logits com sobreposição seguida de argmax garante
bordas suaves entre os patches.

"""

# Carregar melhor modelo
model.load_state_dict(torch.load('best_unet.pth', map_location=DEVICE))
model.eval()
print("Melhor modelo carregado.")

H, W, C = image_blur.shape
ps      = PATCH_SIZE

# Acumuladores para inferência com sobreposição
logits_accum = np.zeros((4, H, W), dtype=np.float32)
count_accum  = np.zeros((H, W),    dtype=np.float32)

INFER_STRIDE = ps // 2   # 50% de sobreposição para suavizar bordas

with torch.no_grad():
    for y in range(0, H - ps + 1, INFER_STRIDE):
        for x in range(0, W - ps + 1, INFER_STRIDE):
            patch = image_blur[y:y+ps, x:x+ps, :]
            patch_tensor = torch.from_numpy(
                patch.transpose(2, 0, 1)
            ).unsqueeze(0).float().to(DEVICE)

            logits = model(patch_tensor).squeeze(0).cpu().numpy()  # (4, ps, ps)

            logits_accum[:, y:y+ps, x:x+ps] += logits
            count_accum[y:y+ps, x:x+ps]     += 1

    # Colunas e linhas da borda direita/inferior (que podem não ter sido cobertas)
    # Última coluna
    if W - ps > 0:
        for y in range(0, H - ps + 1, INFER_STRIDE):
            patch = image_blur[y:y+ps, W-ps:W, :]
            patch_tensor = torch.from_numpy(patch.transpose(2, 0, 1)).unsqueeze(0).float().to(DEVICE)
            logits = model(patch_tensor).squeeze(0).cpu().numpy()
            logits_accum[:, y:y+ps, W-ps:W] += logits
            count_accum[y:y+ps, W-ps:W]     += 1

    # Última linha
    if H - ps > 0:
        for x in range(0, W - ps + 1, INFER_STRIDE):
            patch = image_blur[H-ps:H, x:x+ps, :]
            patch_tensor = torch.from_numpy(patch.transpose(2, 0, 1)).unsqueeze(0).float().to(DEVICE)
            logits = model(patch_tensor).squeeze(0).cpu().numpy()
            logits_accum[:, H-ps:H, x:x+ps] += logits
            count_accum[H-ps:H, x:x+ps]     += 1

# Média dos logits acumulados
count_accum[count_accum == 0] = 1  # evita divisão por zero
logits_accum /= count_accum

# Mapa de predição final
pred_map = np.argmax(logits_accum, axis=0)  # (H, W) com valores 0-3

