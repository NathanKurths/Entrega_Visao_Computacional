"""# **Loss, Otimizador e Scheduler**

Usamos uma combinação de duas funções de loss:

**CrossEntropyLoss:** penaliza erros de classificação por pixel.

**Dice Loss:** otimiza diretamente o coeficiente Dice (métrica de
sobreposição), especialmente útil quando as classes são desbalanceadas
(ex: área de água muito menor que área urbana).

A combinação das duas (CE + Dice) costuma dar melhores resultados
do que qualquer uma isoladamente.

"""

class DiceLoss(nn.Module):
    """Dice Loss para segmentação semântica multiclasse."""
    def __init__(self, num_classes=4, smooth=1.0):
        super().__init__()
        self.num_classes = num_classes
        self.smooth      = smooth

    def forward(self, logits, targets):
        probs = torch.softmax(logits, dim=1)   # (B, C, H, W)
        loss  = 0.0

        for c in range(self.num_classes):
            pred_c   = probs[:, c, :, :]
            target_c = (targets == c).float()

            intersection = (pred_c * target_c).sum()
            union        = pred_c.sum() + target_c.sum()

            dice = (2. * intersection + self.smooth) / (union + self.smooth)
            loss += 1 - dice

        return loss / self.num_classes


class CombinedLoss(nn.Module):
    """CrossEntropy + Dice Loss com peso ajustável."""
    def __init__(self, num_classes=4, ce_weight=0.5, dice_weight=0.5):
        super().__init__()
        self.ce        = nn.CrossEntropyLoss()
        self.dice      = DiceLoss(num_classes)
        self.ce_w      = ce_weight
        self.dice_w    = dice_weight

    def forward(self, logits, targets):
        return self.ce_w * self.ce(logits, targets) + \
               self.dice_w * self.dice(logits, targets)


