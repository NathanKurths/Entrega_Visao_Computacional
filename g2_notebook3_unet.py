"""# **Arquitetura U-Net**

A U-Net é uma rede encoder-decoder com skip connections.
Foi originalmente proposta para segmentação biomédica e é o
estado da arte para segmentação de imagens de satélite.

**Estrutura:**
- Encoder (contração): extrai features progressivamente
- Bottleneck: representação mais comprimida
- Decoder (expansão): reconstrói a resolução espacial
- Skip connections: concatenam features do encoder no decoder,
  preservando detalhes espaciais

**Entrada:** tensor (B, 8, H, W) — 8 canais espectrais
**Saída:**   tensor (B, 4, H, W) — logits para 4 classes

"""

class ConvBlock(nn.Module):
    """Bloco básico: Conv → BN → ReLU → Conv → BN → ReLU"""
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_ch, out_ch, kernel_size=3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch),
            nn.ReLU(inplace=True),
            nn.Conv2d(out_ch, out_ch, kernel_size=3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch),
            nn.ReLU(inplace=True),
        )

    def forward(self, x):
        return self.block(x)


class EncoderBlock(nn.Module):
    """Bloco encoder: ConvBlock + MaxPool"""
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.conv = ConvBlock(in_ch, out_ch)
        self.pool = nn.MaxPool2d(2)

    def forward(self, x):
        skip = self.conv(x)   # guarda para o skip connection
        x    = self.pool(skip)
        return x, skip


class DecoderBlock(nn.Module):
    """Bloco decoder: Upsample + concatenação + ConvBlock"""
    def __init__(self, in_ch, skip_ch, out_ch):
        super().__init__()
        self.up   = nn.ConvTranspose2d(in_ch, out_ch, kernel_size=2, stride=2)
        self.conv = ConvBlock(out_ch + skip_ch, out_ch)

    def forward(self, x, skip):
        x = self.up(x)
        x = torch.cat([x, skip], dim=1)   # skip connection
        return self.conv(x)


class UNet(nn.Module):
    """
    U-Net para segmentação semântica multiespectral.

    Args:
        in_channels  : número de bandas de entrada (8)
        num_classes  : número de classes de saída (4)
        base_filters : número de filtros no primeiro bloco (32 ou 64)
    """
    def __init__(self, in_channels=8, num_classes=4, base_filters=32):
        super().__init__()
        f = base_filters

        # Encoder
        self.enc1 = EncoderBlock(in_channels, f)      # 8 → 32
        self.enc2 = EncoderBlock(f,           f*2)    # 32 → 64
        self.enc3 = EncoderBlock(f*2,         f*4)    # 64 → 128
        self.enc4 = EncoderBlock(f*4,         f*8)    # 128 → 256

        # Bottleneck
        self.bottleneck = ConvBlock(f*8, f*16)        # 256 → 512

        # Decoder
        self.dec4 = DecoderBlock(f*16, f*8,  f*8)    # 512, 256 → 256
        self.dec3 = DecoderBlock(f*8,  f*4,  f*4)    # 256, 128 → 128
        self.dec2 = DecoderBlock(f*4,  f*2,  f*2)    # 128, 64  → 64
        self.dec1 = DecoderBlock(f*2,  f,    f)       # 64, 32   → 32

        # Classificador final
        self.head = nn.Conv2d(f, num_classes, kernel_size=1)

    def forward(self, x):
        # Encoder
        x, s1 = self.enc1(x)
        x, s2 = self.enc2(x)
        x, s3 = self.enc3(x)
        x, s4 = self.enc4(x)

        # Bottleneck
        x = self.bottleneck(x)

        # Decoder com skip connections
        x = self.dec4(x, s4)
        x = self.dec3(x, s3)
        x = self.dec2(x, s2)
        x = self.dec1(x, s1)

        return self.head(x)


