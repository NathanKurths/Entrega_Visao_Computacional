"""# **Dataset de Patches para Treinamento**

A U-Net trabalha com patches (recortes) da imagem para:
- Aumentar a quantidade de amostras de treinamento
- Permitir data augmentation
- Gerenciar o uso de memória da GPU

**Parâmetros:**
- PATCH_SIZE: tamanho do patch em pixels (ex: 64×64)
- STRIDE: deslocamento entre patches (< PATCH_SIZE = sobreposição)

"""

# =====================================================
# >>> PARÂMETROS DO DATASET — AJUSTE SE NECESSÁRIO
PATCH_SIZE  = 64    # tamanho do patch (64 ou 128)
STRIDE      = 32    # stride (sobreposição de 50% com stride=32)
BATCH_SIZE  = 16    # amostras por batch
VAL_SPLIT   = 0.2   # 20% dos patches para validação
# =====================================================

class SentinelPatchDataset(Dataset):
    """
    Dataset que extrai patches da imagem multiespectral e da máscara.

    Args:
        image  : np.ndarray (H, W, C) — imagem normalizada
        mask   : np.ndarray (H, W)    — mapa de classes
        patch_size : int
        stride     : int
        augment    : bool — aplica flip aleatório se True
    """

    def __init__(self, image, mask, patch_size=64, stride=32, augment=False):
        self.image      = image
        self.mask       = mask
        self.patch_size = patch_size
        self.augment    = augment
        self.patches    = []

        H, W = mask.shape
        ps   = patch_size

        # Extrai coordenadas de todos os patches válidos
        for y in range(0, H - ps + 1, stride):
            for x in range(0, W - ps + 1, stride):
                img_patch  = image[y:y+ps, x:x+ps, :]
                mask_patch = mask[y:y+ps, x:x+ps]

                # Ignora patches predominantemente inválidos
                valid_ratio = np.mean(mask_patch >= 0)
                if valid_ratio < 0.5:
                    continue

                self.patches.append((img_patch.copy(), mask_patch.copy()))

        print(f"Total de patches extraídos: {len(self.patches)}")

    def __len__(self):
        return len(self.patches)

    def __getitem__(self, idx):
        img, msk = self.patches[idx]

        # Augmentação: flip horizontal e vertical aleatórios
        if self.augment:
            if np.random.rand() > 0.5:
                img = np.flip(img, axis=1).copy()
                msk = np.flip(msk, axis=1).copy()
            if np.random.rand() > 0.5:
                img = np.flip(img, axis=0).copy()
                msk = np.flip(msk, axis=0).copy()

        # (H, W, C) → (C, H, W) para PyTorch
        img_tensor = torch.from_numpy(img.transpose(2, 0, 1)).float()

        # Máscara: substitui -1 por 0 (pixels inválidos viram classe 0)
        msk = msk.copy()
        msk[msk < 0] = 0
        msk_tensor = torch.from_numpy(msk).long()

        return img_tensor, msk_tensor


