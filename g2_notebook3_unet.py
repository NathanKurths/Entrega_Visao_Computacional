"""# **Métricas de Avaliação: IoU e Dice**

**IoU (Intersection over Union):** mede a sobreposição entre a
predição e o ground truth para cada classe. É a métrica padrão
para segmentação semântica.

**Dice Score:** similar ao IoU, mas pondera mais a sobreposição.
É equivalente ao F1-Score na segmentação.

Avaliamos a U-Net contra o pseudo ground truth. Uma acurácia
superior à do K-Means indica que a U-Net generalizou melhor
os padrões espaciais.

"""

NUM_CLASSES  = 4
CLASS_NAMES  = ['Água', 'Areia', 'Vegetação', 'Urbanização']

def compute_iou_dice(pred, gt, num_classes):
    """Calcula IoU e Dice por classe."""
    iou_list, dice_list = [], []

    for c in range(num_classes):
        pred_c = (pred == c)
        gt_c   = (gt   == c)

        intersection = np.logical_and(pred_c, gt_c).sum()
        union        = np.logical_or(pred_c,  gt_c).sum()
        sum_both     = pred_c.sum() + gt_c.sum()

        iou  = intersection / (union + 1e-8)
        dice = (2 * intersection) / (sum_both + 1e-8)

        iou_list.append(iou)
        dice_list.append(dice)

    return np.array(iou_list), np.array(dice_list)


